package com.delottie;

public class Person {
    

    int pid;
    Passport passportno;
    String name;
    public int getPid() {
        return pid;
    }
    public void setPid(int pid) {
        this.pid = pid;
    }
    public Passport getPassportno() {
        return passportno;
    }
    public void setPassportno(Passport passportno) {
        this.passportno = passportno;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    
}
